function displayCart(array) {
  let cardList = document.getElementById("card-child-list");
  cardList.innerHTML = "";
  // ---------------------------------------------------
  let TotalAmt = array.reduce((a, b) => {   
      return a+ + b.quantity*b.price
  }, 0);
 document.querySelector("#checkout-div  span").textContent = "$" + TotalAmt
  // ---------------------------------------------------
  
  array.forEach((element, index) => {
    
      cardList.innerHTML +=` <div class="cart-child">
      <div class="cart-prod-img">
        <img
          src="${element.image1}"
          alt=""
        />
      </div>
      <div class="cart-prod-detail">
        <div
          class="cart-prod-det-1 "
        >
          <p>${element.name}</p>
          <div class="container d-flex gap-3 cart-quant-div">
            <p onclick="reduceQuantity(${index})">-</p>
            <p>${element.quantity}</p>
            <p onclick="addQuantity(${index})">+</p>
          </div>
        </div>
        <div
          class="cart-prod-det-2 d-flex flex-column  justify-content-xxl-between"
        >
          <p>${element.price}</p>
          <p onclick="deleteItem(${index})" id="remove-item">Remove</p>
        </div>
      </div>
    </div>`
  })


  

}



function debounceALl() {
  let timer;
let searchBar = document.getElementById("searchright");
  searchBar.addEventListener("keyup", () => {
   

    debounce(getdata, 200);
  });
  function debounce() {
    if (timer) {
      clearTimeout(timer)
    }
    timer = setTimeout(() => {
      getdata();
    }, 500);
  }

  async function getdata() {
    let searchItem = document.getElementById("searchright").value;
    if (searchItem == "") {
      let showResult = document.getElementById("show-result");

      showResult.style.display = "none";
      return
    }
    try {
      let SearchResult = await fetch(
        `https://project-1-1qlk.onrender.com/glossier?q=${searchItem}`
      );
      let searchOutput = await SearchResult.json();
      displaySearchResult(searchOutput);
      console.log(searchOutput)
     
    } catch (error) {
      console.log(error);
    }
    
}
  
function displaySearchResult(array) {
    let showResult = document.getElementById("show-result");

  showResult.style.display = "block";
 
  showResult.innerHTML = "";

  let arr = array.slice(0, 5);
  arr.forEach((element) => {
    showResult.innerHTML += ` 
    <div class="search-res-card">
      <div>
         <img src="${element.image1}" alt="">
      </div>
         <p>${element.name}</p>
  </div>`
  })
}

document.querySelector("body").addEventListener("click", () => {
  let showResult = document.getElementById("show-result");

  showResult.style.display = "none";
})
}



